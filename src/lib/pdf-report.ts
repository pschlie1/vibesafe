/* ── Types ── */

interface ReportFinding {
  code: string;
  severity: string;
  status: string;
  title: string;
  category: string;
  firstDetectedAt: Date | null;
  resolvedAt: Date | null;
}

interface ReportApp {
  name: string;
  url: string;
  status: string;
  score: number;
  findings: ReportFinding[];
  scanCount: number;
  lastScanAt: Date | null;
}

type Framework = "soc2" | "iso27001" | "nist";

